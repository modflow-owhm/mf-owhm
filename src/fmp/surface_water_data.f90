      LOGICAL:: BUILD_FULLY_ROUTED_RETURN = FALSE  ! = ANY(BUILD_FRR)
      LOGICAL:: CMD_RUNOFF_PRNT = TRUE
      LOGICAL:: HAS_ADDED_RUNOFF = FALSE
