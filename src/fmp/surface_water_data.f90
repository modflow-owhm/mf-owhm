    DO CONCURRENT (F=ONE:SWF%NFARM, SWF%SRDLOC(F)%N > Z) 
      DO CONCURRENT (I = ONE:SWF%SRDLOC(F)%N)
            !
      SWF%SRDLOC(F)%FLOW(I)   = DZ
      SWF%SRDLOC(F)%MAXDEL(I) = DZ
      END DO
    END DO
    !
  END SUBROUTINE
